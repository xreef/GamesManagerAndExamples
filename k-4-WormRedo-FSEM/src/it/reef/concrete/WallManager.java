	private void createListOfWallFromMatrix(int[][] m){
		for (int col=0;col<m[1].length;col++){
			for (int row=0;row<m.length;row++){
				if (m[row][col]==1){
					Wall w = new Wall(col*dotSize,row*dotSize,dotSize,dotSize);
					listWall.add(w);
				}
			}
		}
	}

	public void draw(Graphics g) {
		for(int i=0;i<listWall.size();i++){
			listWall.get(i).draw(g);
		}
	}

	public java.util.List<Wall> getListWall() {
		return listWall;
	}
	
	public boolean detectWallCollision(Point newPt, int DOTSIZE){
		List<Wall> lw = this.getListWall();
		for (int i=0;i<lw.size();i++){
			if (lw.get(i).hit(newPt, DOTSIZE)) return true;
		}
		return false;
	}
